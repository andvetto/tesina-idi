            <div class="p-5">
              <p>Sandra and Stefano Giusti were born respectively in 1962 and 1959. They began working with ceramics in 1980 in Florence, under the guidance of the sculptor potter Giancarlo Girard, with whom they later collaborated for about two years. They attended the terracotta school of Impruneta. Together with other potters, they opened a workshop in the area, which later moved to Pontassieve. They collaborated on the realisation of limited editions for artists. Their interest in ceramics for everyday use began to crystalise and materialised within the framework of the <a href="http://lafierucola.org" target="_blank">La Fierucola</a> association.</p>

				<p>In 1989 the workshop was moved to Paterno di Pelago, in the hills around Florence. The production was focuesd particularly on objects of everyday use, where the values of tradition are perfectly blended with a contemporary language. They have created tableware and accessory ranges for Haas and Mastro Raphael. They also collaborated with the “Ardengo Soffici” association of artists for the creation of the ceramic section of a monument set up at the Gates of Florence. As well as creating objects, they also hold courses, both at their own studio and for private and public structures within the territory.
				They were the inspiration behind the <a href="http://www.artedellaceramica.net" target="_blank">Arte della Ceramica</a> potters’ association which they set up as founder members in 2004. </p>
				<p>Since 2000, they have promotors of the <a href="https://www.facebook.com/fieradellaceramicafirenze" target="_blank">International Ceramics Fair</a> that is held every year in Florences, bringin togethere eighty potters from different parts of Europe. Their works have been displayed in various cities, including Florence, San Gimignano, Paris, Munich and Nanking. They also displayed at the Florence Fiera dell’Artigianato between 1987 and 1990, when their articles were chosen for the national advertising campaign. Over the last year they have displayed at the Craft Gallery at the Fortezza da Basso in Florence, at the Contemporary Ceramics Fair, “Eros”, Montelupo F.no, at the Craft Gallery in Frankfurt, at the Hotel Astoria, Firenze, Artour project, at the Macef in Milan, and at a collateral show in the itinerant event “Wine e Fashion”.</p>


