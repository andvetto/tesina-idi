              <p>Sandra e Stefano Giusti sono nati rispettivamente nel 1962 e nel 1959. Iniziano ad occuparsi di ceramica nel 1980, a Firenze, sotto la guida dello scultore ceramista Giancarlo Girard, con il quale collaboreranno, in seguito, per circa due anni. Frequentano la scuola del cotto all’Impruneta. In questa località, assieme ad altri ceramisti aprono un laboratorio che in seguito viene trasferito a Pontassieve. Collaborano alla realizzazione di serie limitate di artisti. Inizia anche a delinearsi il loro interesse per la ceramica di uso quotidiano che si concretizza nell’ambito dell’associazione <a href="http://lafierucola.org" target="_blank">La Fierucola</a>. </p>

